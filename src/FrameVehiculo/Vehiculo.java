    private void btnPuerIzquiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPuerIzquiActionPerformed
        if (!PuerIzqAbierta) {
            puertaIzq.abrirPuerta();
            lblLuzIz.setText("Luz PuerIzquierda encendida");
            btnPuerIzqui.setText("Cerrar PuerIzquierda");
            PuerIzqAbierta = true;
            lblPuertIzq.setVisible(true);
            lblPuerIzq.setVisible(false); 
        } else {
            puertaIzq.cerrarPuerta();
            lblLuzIz.setText("Luz PuerIzquierda apagada");
            btnPuerIzqui.setText("Abrir PuerIzquierda");
            PuerIzqAbierta = false;
            lblPuertIzq.setVisible(false);
            lblPuerIzq.setVisible(true);
        }
        if (PuerIzqAbierta || PuerDerAbierta) {
            lblLuzPuertas.setVisible(true);
        } else {
            lblLuzPuertas.setVisible(false);
        }
    }//GEN-LAST:event_btnPuerIzquiActionPerformed

