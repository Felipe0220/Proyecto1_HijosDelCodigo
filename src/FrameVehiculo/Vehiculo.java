    private void btnEmergenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmergenciaActionPerformed
        if (!motor.isEstado()) {
            btnEmergencia.setSelected(false);
            javax.swing.JOptionPane.showMessageDialog(this, "Enciende el motor para usar luces de emergencia");
            return;
        }
        if (btnEmergencia.isSelected()) {
            if (chkIntIzqui.isSelected() || chkIntDer.isSelected()) {
                btnEmergencia.setSelected(false);
                javax.swing.JOptionPane.showMessageDialog(this, "Apaga las intermitentes para activar las luces de emergencia");
                return;
            }
            intermitentes.activarEmergencia(motor);
            chkIntIzqui.setSelected(true);
            chkIntDer.setSelected(true);
            btnEmergencia.setText("Apagar emergencia");
            lblIntIzq.setVisible(true);
            lblIntDer.setVisible(true);
        } else {
            intermitentes.apagarEmergencia();
            chkIntIzqui.setSelected(false);
            chkIntDer.setSelected(false);
            btnEmergencia.setText("Encender emergencia");
            lblIntIzq.setVisible(false);
            lblIntDer.setVisible(false);
        }
    }//GEN-LAST:event_btnEmergenciaActionPerformed

